import type { SVGProps } from 'react';

/**
 * Inline replacements for the handful of `@iconscout/react-unicons` glyphs the
 * site actually uses. Dropping the dependency keeps the React icon components
 * out of the client bundle (Nav, Modal, BuyMeCrypto are client islands) — the
 * SVG markup is identical to the originals (Unicons line set, 24×24 viewBox).
 *
 * API matches the original: `({ color = 'currentColor', size = 24, ...rest })`,
 * so call sites only change their import path. `rest` is spread last so callers
 * can override width/height (Nav passes `width={28}`) or pass id/className.
 */
export interface UilIconProps extends Omit<SVGProps<SVGSVGElement>, 'color'> {
  color?: string;
  size?: string | number;
}

function UilIcon({
  d,
  color = 'currentColor',
  size = 24,
  ...rest
}: UilIconProps & { d: string }) {
  // Decorative by default — the wrapping button/anchor carries the label. When a
  // caller does supply an accessible name (e.g. the "Copied" check), expose the
  // glyph as an image instead of hiding it from assistive tech.
  const labelled =
    rest['aria-label'] != null || rest['aria-labelledby'] != null;
  return (
    // biome-ignore lint/a11y/noSvgWithoutTitle: decorative glyph — aria-hidden unless a caller passes an accessible name (then role="img"); the wrapping control otherwise carries the label
    <svg
      xmlns="http://www.w3.org/2000/svg"
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill={color}
      role={labelled ? 'img' : undefined}
      aria-hidden={labelled ? undefined : true}
      {...rest}
    >
      <path d={d} />
    </svg>
  );
}

